package com.hrm.commons;

import java.util.List;

/**
 * The Common Response
 * 
 * @author Admin
 *
 */
public class CommonResponse {

	/** The response data */
	private List<?> data;
	
	/** The total of all employee*/
	private long total;

	/** The status of response */
	private boolean status;

	/** The status code of response */
	private int statusCode;

	/** The message of response */
	private String message;

	/** The message validate of response */
	private List<?> messageValidate;

	public List<?> getData() {
		return data;
	}

	public void setData(List<?> data) {
		this.data = data;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

	public boolean isStatus() {
		return status;
	}

	public void setStatus(boolean status) {
		this.status = status;
	}

	public int getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(int statusCode) {
		this.statusCode = statusCode;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public List<?> getMessageValidate() {
		return messageValidate;
	}

	public void setMessageValidate(List<?> messageValidate) {
		this.messageValidate = messageValidate;
	}

}
